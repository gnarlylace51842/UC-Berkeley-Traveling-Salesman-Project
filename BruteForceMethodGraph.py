def solve_bruteForce(distances):
    start = time.time()
    nodes = list(distances.keys())
    possiblePaths = []
    costAndIndex = []

    for p in permutations(nodes):
        if p[0]==nodes[0]:
            path = list(p)
            path.append(nodes[0])
            possiblePaths.append(path)

    for path in possiblePaths:
        total_cost = 0
        for i in range(len(path) - 1):
            from_city = path[i]
            to_city = path[i + 1]
            total_cost += distances[from_city][to_city]
        costAndIndex.append((total_cost, path))

    min_cost = float('inf')
    min_cost_path = None
    for cost, path in costAndIndex:
        if cost < min_cost:
            min_cost = cost
            min_cost_path = (cost, path)

    end = time.time()

    times.append(end-start)

    # print("Elapsed Time: "+str(end-start)+" seconds.")
    # print("Best path:", min_cost_path[1])
    # print("Minimum cost:", min_cost_path[0])

for nodes in distances:
    solve_bruteForce(nodes)

