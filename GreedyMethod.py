def euclidean(a, b):
    return ((a[0] - b[0])**2 + (a[1] - b[1])**2)**0.5

def greedy_tsp(nodes):
    start = nodes[0]
    unvisited = nodes[1:]
    path = [start]
    current = start

    while unvisited:
        next_node = min(unvisited, key=lambda x: euclidean(current, x))
        path.append(next_node)
        unvisited.remove(next_node)
        current = next_node

    path.append(start)
    return path

tsp_path = greedy_tsp(nodes)

def total_path_distance(path):
    return sum(euclidean(path[i], path[i+1]) for i in range(len(path) - 1))

